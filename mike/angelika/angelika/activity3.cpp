#include <iostream>
using namespace std;

int main() {
    double prelim = 80, midterm = 90, prefinal = 95, final = 85;
    double average = (prelim + midterm + prefinal + final) / 4;
    cout << "Prelim: " << prelim << endl;
    cout << "Midterm: " << midterm << endl;
    cout << "Prefinal: " << prefinal << endl;
    cout << "Final: " << final << endl;
    cout << "Average: " << average << endl;
    return 0;
}
    